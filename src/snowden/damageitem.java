    void display() {
        try {
            Connection com = DB.conect();
            ResultSet r = com.createStatement().executeQuery("select * from damage");
            Vector v = new Vector();
            while (r.next()) {
                String a = r.getString("damage_id");
                String b = r.getString("emp_id");
                String c = r.getString("description");
                String d = a + "-" + b + "-" + c;
                v.add(d);

            }
            jList1.setListData(v);
        } catch (Exception e) {
            //    e.printStackTrace();
        }
    }

