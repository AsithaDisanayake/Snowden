    void atuono() {
        try {
            Connection com = DB.conect();
            ResultSet r = com.createStatement().executeQuery("select emp_id from employee");
            int max = 1000;
            boolean b = false;
            while (r.next()) {
                b = true;
                int x = Integer.parseInt(r.getString("emp_id").trim());
                if (max < x) {
                    max = x;
                }


            }
            if (b) {
                jTextField1.setText(max + 1 + "");
            } else {
                jTextField1.setText("1000");

            }

        } catch (Exception e) {

            e.printStackTrace();
        }
    }

